public class array {
    public static void main(String[] args) {
        // Buat variabel dengan tipe data array
        int[] numbers = {1, 2, 3, 4, 5};

        // Gunakan perulangan for untuk menampilkan semua nilai dalam variabel array
        for (int number : numbers) {
            System.out.println(number);
        }
    }
}

